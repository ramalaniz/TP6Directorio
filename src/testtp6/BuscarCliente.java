    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        String telefono = jtTelefono.getText();
        Cliente cli = Escritorio.getDirectorio().buscarCliente(telefono);

        Directorio agenda = Escritorio.getDirectorio();
        if(agenda.buscarCliente(telefono)!=null){
        //agenda.buscarCliente(telefono);
        this.jtDni.setText(cli.getDni());
        this.jtApellido.setText(cli.getApellido());
        this.jtNombre.setText(cli.getNombre());
        this.jtCiudad.setText(cli.getCiudad());
        this.jtDomicilio.setText(cli.getDireccion());
        }
        else{
            JOptionPane.showMessageDialog(this,"No se encontraron datos");
        }
    
    }//GEN-LAST:event_jbBuscarActionPerformed

